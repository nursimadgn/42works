#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	if (n == 0)
	{
		return (0);
	}
	while (s1[i] != '\0' && s2[i] != '\0' && i < n - 1)
	{
		if (s1[i] == s2[i])
		{
			i++;
		}
		else
		{
			return (s1[i] - s2[i]);
		}
	}
	return (s1[i] - s2[i]);
}

int	main(void)
{
	char	array1[] = "a";
	char	*ptr;
	int		deneme;

	ptr = "c";
	deneme = ft_strncmp(array1, ptr, 1);
	printf("%d", deneme);
	return (0);
}
